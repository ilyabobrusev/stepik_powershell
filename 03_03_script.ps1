# Запрашиваем у пользователя число N
$N = Read-Host "Введите число N для вычисления факториала (N!)"

# Проверяем, что введено целое неотрицательное число
if (-not ($N -match '^\d+$')) {
    Write-Host "Ошибка: Введите целое неотрицательное число!"
    exit
}

# Преобразуем в число
$N = [int]$N

# Вычисляем факториал
$factorial = 1

if ($N -eq 0) {
    $factorial = 1
} else {
    for ($i = 1; $i -le $N; $i++) {
        $factorial *= $i
    }
}

# Выводим результат
Write-Host "$N! = $factorial"
